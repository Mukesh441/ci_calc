"""
This module contains basic arithmetic functions such as addition and subtraction.
"""


def add(a, b):
    """Add two numbers."""
    return a + b

def subtract(a, b):
    """Subtract two numbers."""
    return a - b

if __name__ == "__main__":
    print("Addition of 3 and 5:", add(3, 5))
    print("Subtraction of 10 and 4:", subtract(10, 4))
    